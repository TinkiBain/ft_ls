#include "libft.h"

char	*ft_strcat(char *str1, const char *str2)
{
	int i;

	i = 0;
	while (str1[i] != '\0')
		i++;
	while (*str2 != '\0')
	{
		str1[i] = *str2;
		i++;
		str2++;
	}
	str1[i] = '\0';
	return (str1);
}
