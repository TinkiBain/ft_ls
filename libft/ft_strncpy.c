#include "libft.h"

char	*ft_strncpy(char *str1, const char *str2, size_t n)
{
	size_t i;

	i = 0;
	while (i < n && str2[i] != '\0')
	{
		str1[i] = str2[i];
		i++;
	}
	while (i < n)
	{
		str1[i] = '\0';
		i++;
	}
	return (str1);
}
